# mantıksal operatörler

x, y = False, True


print(x and y)                  # her iki ifadede true sonuca eşitse true sonucu verir. aksi halde false sonucu verir.
print((7 > 5) and (8 > 6))

print(x or y)                   # iki ifadeden biri true sonuca eşitse, true sonucu verir, aksi halde false sonucu verir.
print((6 < 3) or (8 < 2))


